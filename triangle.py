def area(a, h):
    '''Возвращает площадь треугольника
    Пример вызова:
    area(5, 4) вернет 10'''
    if a < 0 or h < 0:
        raise ValueError("Основание и высота треугольника не могут быть отрицательными")
    return a * h / 2

def perimeter(a, b, c):
    '''Возвращает периметр треугольника
    Пример вызова:
    perimetr(5, 4, 3) вернет 12'''
    if a < 0 or b < 0 or c < 0:
        raise ValueError("Стороны треугольника не могут быть отрицательными")

    # Проверка неравенства треугольника (только для положительных значений)
    if a > 0 and b > 0 and c > 0:
        if a + b <= c or a + c <= b or b + c <= a:
            raise ValueError("Треугольник с такими сторонами не существует")

    return a + b + c